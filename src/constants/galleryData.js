// Utility functions for filtering and retrieving projects
export const getProjectsByCategory = (categoryId) => {
  if (categoryId === 'all') return galleryProjects
  return galleryProjects.filter(project => project.category === categoryId)
}

export const getAllProjects = () => galleryProjects

export const getProjectById = (id) => {
  return galleryProjects.find(project => project.id === id)
}

export const getProjectsByTags = (tags) => {
  return galleryProjects.filter(project =>
    tags.some(tag => project.tags.includes(tag))
  )
}