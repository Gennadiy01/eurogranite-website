// Функція для отримання SEO даних для конкретної сторінки
export const getSeoData = (language, page) => {
  const pageName = page === '' ? 'home' : page
  return staticSeoData[language]?.[pageName] || staticSeoData.ua.home
}

// Функція для генерації hreflang тегів
export const generateHreflangTags = (page) => {
  const languages = ['ua', 'en', 'de', 'pl']

  return languages.map(lang => {
    const url = lang === 'ua'
      ? `${BASE_URL}/${page ? page + '/' : ''}`
      : `${BASE_URL}/${lang}/${page ? page + '/' : ''}`

    const hreflang = lang === 'ua' ? 'uk' : lang
    return `  <link rel="alternate" hreflang="${hreflang}" href="${url}" />`
  }).join('\n')
}